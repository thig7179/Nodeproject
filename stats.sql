CREATE TABLE CIDADE (
  CID_CODIGO       int(11) PRIMARY KEY AUTO_INCREMENT,
  CID_DESCRICAO    varchar(15) NOT NULL,
  CID_ESTADO       varchar(2)  NOT NULL,
  CID_HABITANTES   int(11)     NOT NULL,
  CID_ECONOMICA    varchar(10) NOT NULL,
  CID_ANO_FUNDACAO int(11)     NOT NULL,
  REG_COD          int(11),
  KEY REG_COD_FK (REG_COD),
  CONSTRAINT REG_COD_FK FOREIGN KEY (REG_COD) REFERENCES REGIAO (REG_CODIGO)
);

