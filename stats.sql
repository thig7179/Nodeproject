USE ESTATISTICA;

CREATE TABLE REGIAO (
  REG_CODIGO    int(11) PRIMARY KEY AUTO_INCREMENT,
  REG_DESCRICAO varchar(10)    NOT NULL,
  REG_SIGLA     varchar(2)     NOT NULL,
  REG_AREA_KM2  int(11)        NOT NULL,
  REG_VALOR_PIB decimal(15, 2) NOT NULL
);

